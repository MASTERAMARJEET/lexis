<H1>Verse</H1>
<P>
  A pen, a paper and a few words. That's all it takes for a downpour of emotions
  on paper. Poetry is a country, a form, a language at its most powerful and
  pristine form, a tempest that has the vigour to touch the soul with even a
  single verse.
</P>
<P>
  Verse, an on-spot poetry writing competition and recitation session, is an
  event for everyone who loves and appreciates poetry. Whether you are
  experienced or want to write your first poem, you are welcome to participate
  in this event to express your creativity and thoughts. Come, learn and let's
  create poetry together.
</P>

<H2>Rules</H2>
<P>
  The event consists of one round. It is a writing and recitation round where
  participants will write a poem on the spot for 45 mins based on a theme given
  to them and then recite what they wrote. Languages allowed for the competition
  are English, Hindi and Bengali. The participants will be evaluated based on
  their ideas, expression, clarity of thought and relevance to the theme.
  Participants who regularly write or are involved in poetry recitation are
  welcome to share their favourite poems with the crowd(original or
  reproductions) and will not be evaluated based on them.
</P>
