<H2>Rules</H2>
<LIST>
  <li>Participants will be given a sequence of four or more images. Their task is to write a long story connecting these pictures . Entries will be judged based on how uniquely they incorporated the essence of the images. (word limit - 1000 words)</li>

  <li>Short worldbuilding exercise in joining two entirely disjoint genres - write a brief POV of a person living in a world that incorporates the genres Climate fiction and cyberpunk. What does a day in their life typically sound like? What adventures do they get up to? Let your imagination have no bounds! (word limit - 500 words)</li>

  <li> Inspired by the famous Netflix show "Love, Death and Robots", we invite you before the Production Board at The Institute of Science (Education) Fiction and Research! Get ready to pitch your show and write a script for the pilot episode to convince the judge. Bonus points if you can leave us wanting for more! (Time of screenplay ~15 minutes)
  </li>

  <li>
    Every author has found themselves completely immersed in their stories from time to time - it's almost like entering a new world in itself! Participants are invited to submit a small poem about an author who finds themselves stuck in a science fiction worldscape that they created - will their worldbuilding decision work in their favour or spell out their doom?
  </li>
