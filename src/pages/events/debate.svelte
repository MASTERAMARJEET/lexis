<H1>War of Words</H1>
<H2>11:00 AM on 28th Aug @ G05</H2>
<P>
  Do you often find yourselves in an argument with your friends, family or ‘tus
  amantes’? It’s time to showcase your convincing powers and win over, not just
  your friend, but a whole audience. And don’t worry, we would not force you
  with a side that you don’t connect with. For the first time (since ancient
  Romans probably), you will choose a side and will speak against someone who
  has an opposing view. We are done with cliche topics and boring old debates;
  this time we have very neutral and personal topics that bring out who you are
  as a person. Rest assured, you would not wanna miss this one for anything!
</P>
<H2>Registration</H2>
<List>
  <li>Each team will have 2-3 members.</li>
  <li>
    The topics will be given a day before the event and then there will be
    voting the same in the evening in which the teams will choose their side on
    each topic (there will be a neutral option as well, so fret not).
  </li>
  <li>
    Then they will be assigned one of the topics based on what they had
    preferred and will be pitted against one of the teams that chose the
    opposite. The latter process of selection would be random.
  </li>
</List>
<H2>Rules</H2>
<List>
  <li>
    One of the members of the team that is speaking for the motion will present
    the opening argument that should not be more than 4 minutes. There will be a
    warning bell 30 seconds before and the final one at 4 minutes.
  </li>
  <li>
    Then one of the speakers from the opposing team will begin and present their
    argument. He/She can try to refute some of the statements by the first
    speaker if he/she wants to. This will again be for 4 minutes.
  </li>
  <li>
    The next round will be QnA where each team gets to ask questions (one by
    one) to each other. It is important for all the speakers to take turns while
    questioning and answering. The round will be 5-7 minutes long.
  </li>
  <li>
    Depending on the participation, there will be an audience/judge round of 3-5
    minutes, where questions are invited. I would not disclose the name of the
    judge because that would not be in the right spirit (Sayam Sir).
  </li>
</List>
