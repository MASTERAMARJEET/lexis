<H1>Writing Lab</H1>
<H2>10:00 AM on 27th Aug @ G02</H2>
<P>
  To seek new ways to write, to search for our most creative sources, we need to
  hold our initial impulses back. You may have wanted to write something but do
  not know where to begin. After all, it seems anything at all can be written
  down. But, come and embrace the limitations to find what indeed emerges from
  within. Join us in finding springs of inspiration in constraints and rules and
  in becoming rats who construct the labyrinth from which they plan to escape.
</P>

<H2>Rules</H2>

<List>
  <li>
    All participants are provided with a piece of paper and pens. Each
    participant is also given one sticky note per round.
  </li>
  <li>
    Each round involves a constraint or writing prompt, which has to be used,
    and a final piece has to be written on a sticky note. The paper can be used
    for brainstorming.
  </li>
  <li>Each round lasts 5 minutes, and a total of 10 rounds will be held.</li>
  <li>
    At the end of each round, all the sticky notes will be posted on a poster
    board.
  </li>
</List>

